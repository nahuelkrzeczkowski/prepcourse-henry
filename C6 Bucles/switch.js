var color = "amarillo";

switch(color) {
    case "rojo":
        console.log("es color rojo");
        break
    case "amarillo":
        console.log("es color amarillo");
        break
    case "azul":
        console.log("es color azul");
        break
    default:
        console.log("no es un color")
        break
} 


